package com.cryptoforge.utils;

import com.cryptoforge.model.HistoryItem;
import com.cryptoforge.model.OperationDetail;
import com.cryptoforge.model.SecretVisibility;
import java.util.List;

/** Produces a human-readable history report without weakening secret visibility. */
public final class HistoryReportExporter {
    private HistoryReportExporter() { }

    public static String toMarkdown(HistoryItem item, SecretVisibility visibility) {
        if (item == null) throw new IllegalArgumentException("History item is required");
        SecretVisibility policy = visibility == null ? SecretVisibility.REDACTED : visibility;
        StringBuilder report = new StringBuilder("# CryptoCarver operation report\n\n");
        report.append("- **Operation:** ").append(escapeInline(item.getOperation())).append('\n');
        report.append("- **Recorded:** ").append(escapeInline(item.getTimestamp())).append('\n');
        report.append("- **Secret visibility:** ").append(policy).append("\n\n");
        report.append("## Details\n\n");

        List<OperationDetail> details = item.getStructuredDetails();
        if (details == null || details.isEmpty()) {
            return report.append("No structured details are available for this legacy history entry.\n").toString();
        }
        report.append("| Property | Class | Format | Value |\n|---|---|---|---|\n");
        for (OperationDetail detail : details) {
            if (detail == null || isOmitted(detail, policy)) continue;
            report.append('|').append(escapeInline(detail.name())).append('|')
                    .append(detail.classification()).append('|')
                    .append(escapeInline(detail.format())).append('|')
                    .append(escapeInline(visibleValue(detail, policy))).append("|\n");
        }
        return report.append("\n> Generated by CryptoCarver. Review this report before sharing it.\n").toString();
    }

    private static boolean isOmitted(OperationDetail detail, SecretVisibility visibility) {
        return detail.classification() == OperationDetail.Classification.SECRET
                && visibility == SecretVisibility.REDACTED;
    }

    private static String visibleValue(OperationDetail detail, SecretVisibility visibility) {
        if (detail.classification() == OperationDetail.Classification.SECRET && visibility == SecretVisibility.MASKED) return "***MASKED***";
        if (detail.classification() == OperationDetail.Classification.SENSITIVE && visibility != SecretVisibility.FULL_LAB) return "***MASKED***";
        return detail.value() == null ? "" : detail.value();
    }

    private static String escapeInline(String value) {
        return (value == null ? "" : value).replace("\\", "\\\\").replace("|", "\\|")
                .replace("\r\n", "<br>").replace("\n", "<br>").replace("\r", "<br>");
    }
}
